function add(a, b) {
    return(a + b);
}

function subtract(a, b) {
    return(a - b);
}

function multiply(a, b) {
    return(a * b);
}

function divide(a, b) {
    return(a / b)
}

function increment(n) {
    return(n + 1)
}

function decrement(n) {
    return(n - 1)
}
function makeInt(n, r = 10) {
    return parseInt(n, r)
}
function preserveDecimal(n, r = 10) {
    return parseFloat(n, r)
}